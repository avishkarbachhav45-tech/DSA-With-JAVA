public class TypeCasting {
    public static void main(String args[]){
        float number = 99.999f;
        int number2 = (int) number;
        System.out.println(number2);

        char ch = 'a';
        int num = ch;
        char ch2 = 'b';
        int num2 = ch2;
        System.out.println(num);
        System.out.println(num2);
    }
}
